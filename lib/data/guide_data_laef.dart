// ─────────────────────────────────────────────────────────────────────────────
// Helper — unchanged task definitions reuse the same IDs as guide_data.dart
// ─────────────────────────────────────────────────────────────────────────────
GuideTask _t(
  String id,
  String name, {
  int? points,
  TaskDifficulty diff = TaskDifficulty.easy,
  List<String> tips = const [],
}) =>
    GuideTask(
      id: id,
      name: name,
      points: points ?? diff.defaultPoints,
      difficulty: diff,
      tips: tips,
    );

